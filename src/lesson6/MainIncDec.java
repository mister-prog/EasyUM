package lesson6;

import java.util.Arrays;

public class MainIncDec {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("i = " + i);//1
        System.out.println(i++); //1
        System.out.println(++i); //2 + 1 => 3
        System.out.println(i); //3
    }
}
